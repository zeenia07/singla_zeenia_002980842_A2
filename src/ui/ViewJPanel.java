    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int selectedRowIndex = tblViewCars.getSelectedRow();
        
        if(selectedRowIndex<0) {
            JOptionPane.showMessageDialog(this, "Please select a row to delete.");
            return;
        }
        
        DefaultTableModel model = (DefaultTableModel) tblViewCars.getModel();
        Car selectedCar = (Car) model.getValueAt(selectedRowIndex,0);
        
        history.deleteCars(selectedCar);
        
        JOptionPane.showMessageDialog(this, "Selected car details deleted");
        
        populateTable();
        
    }//GEN-LAST:event_btnDeleteActionPerformed

