    private void populateTable() {
        DefaultTableModel model = (DefaultTableModel)tblViewCars.getModel();
        model.setRowCount(0);
        
        for(Car c : history.getCarList()){
            
            Object[] row = new Object[9];
            row[0] = c;
            row[1] = c.getBrand();
            row[2] = c.getModelNo();
            row[3] = c.getManufacturing_date();
            row[4] = c.getNumOfSeats();
            row[5] = c.getCity();
            row[6] = c.getMaintCertiExpiryDate();
            row[7] = c.getAvailability();
            row[8] = "";
            
            model.addRow(row);
                    
        }
    }
    
